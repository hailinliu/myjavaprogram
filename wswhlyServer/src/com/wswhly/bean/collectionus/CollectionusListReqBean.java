package com.wswhly.bean.collectionus;

public class CollectionusListReqBean {

	private int pageno;//页数
	private int pagesize;//每页数
	private long typeid;//藏品大分类
	private long styleid;//藏品小分类
	private int pricearea;//1,0=5万 2,1=5至10万 3,2=10至20万 4,3=20至50万 5,4=50至100万 6,5=100万以上
	
	public int getPageno() {
		return pageno;
	}
	public void setPageno(int pageno) {
		this.pageno = pageno;
	}
	public int getPagesize() {
		return pagesize;
	}
	public void setPagesize(int pagesize) {
		this.pagesize = pagesize;
	}
	public long getTypeid() {
		return typeid;
	}
	public void setTypeid(long typeid) {
		this.typeid = typeid;
	}
	public long getStyleid() {
		return styleid;
	}
	public void setStyleid(long styleid) {
		this.styleid = styleid;
	}
	public int getPricearea() {
		return pricearea;
	}
	public void setPricearea(int pricearea) {
		this.pricearea = pricearea;
	}
}
